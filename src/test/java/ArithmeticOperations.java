public class ArithmeticOperations {

    public int getMultiply(int number1, int number2) {
        return number1 * number2;
    }

    public int getAddition(int number1, int number2) {
        return number1 + number2;}

    public int getSubtraction(int num1, int num2) {
        return num2 - num1;
    }

    public int getRemainder(int number1, int number2) {
        return number1/number2;
    }

}